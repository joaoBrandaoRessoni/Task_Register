    private void btnCriarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCriarMouseClicked
        String descricao = jtxtdescricao.getText();
        int prioridade = jsliderprioridade.getValue();
        boolean error = false;
        LocalDate data = null;
        LocalDate prazo = null;
        
        if(descricao.equals("")){
            error = true;
            JOptionPane.showMessageDialog(null, "O campo descricao está vazio");
        }

        try{
            data = LocalDate.parse(jformatdata.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        }catch(Exception e){
            error = true;
            JOptionPane.showMessageDialog(null, "O campo data está vazio");
        }
        
        try{
            prazo = LocalDate.parse(jformatprazo.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        }catch(Exception e){
            prazo = null;
        }
        
        if(!error){
            boolean resp = false;
        
            if(prazo != null){
                //Adicionar uma tarefa com prazo
                resp = new TarefaController().addTarefa(descricao, prioridade, data, prazo);
            }else{
                //Adicionar uma tarefa simples
                resp = new TarefaController().addTarefa(descricao, prioridade, data);
            }

            JOptionPane.showMessageDialog(null, 
                    (resp) ? "Cadastrado" : "Erro ao cadastrar");

            if(resp){
                atualizarTabela();
                jtxtdescricao.setText("");
                jsliderprioridade.setValue(10);
                jformatdata.setText("");
                jformatprazo.setText("");
            }
        }
    }//GEN-LAST:event_btnCriarMouseClicked

