    private void JBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnExcluirActionPerformed
        // TODO add your handling code here:
        int index = -1;
        try{
            index = Integer.parseInt(JRemoverIndex.getText()) - 1;
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "O valor informado nao e um numero valido");
            index = -1;
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ocorreu um erro a pergar o valor informado");
            index = -1;
        }

        try{
            int count = new TarefaController().show().size();
            if(index != -1 && count > 0){
                Tarefa tarefa = new TarefaController().index(index);
                boolean deletado = new TarefaController().remove(tarefa);
                
                if(deletado){
                    JOptionPane.showMessageDialog(null, "Tarefa excluida com sucesso");
                    atualizarTabela();
                }else{
                    JOptionPane.showMessageDialog(null, "Nao foi possivel excluir a tarefa");
                }
                
            }else{
                JOptionPane.showMessageDialog(null, "Parece que a tarefa escolhida nao existe");
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao tentar excluir a tarefa");
        }
        
    }//GEN-LAST:event_JBtnExcluirActionPerformed

    /**
     * Esta função remove todas as linhas da tabela e as coloca novamente porém com a lista de tarefas atualizada 
     */
    public void atualizarTabela(){
        //Pega o modelo para poder adicionar as linhas
        DefaultTableModel model = (DefaultTableModel) JTableTarefas.getModel();
        int countRow = model.getRowCount();
        //Remove as linhas da tabela
        for(int i = countRow-1; i>=0; i--){
            model.removeRow(i);
        }
        
        //Adciona as linhas na tabela
        List<Tarefa> lista = new TarefaController().show();
        lista.forEach((t) -> {
            String concluidaTabela = (t.isConcluida()) ? "Concluida" : "Nao concluida";
            String prazoTabela = "Nao possui prazo";
            if(t instanceof TarefaComPrazo){
                prazoTabela = ((TarefaComPrazo) t).getPrazo().format(DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            }
            String dataCriacaoTabela = t.getDataCriacao().format(DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            
            model.addRow(new Object[]{ t.getDescricao(), t.getPrioridade(), concluidaTabela, prazoTabela, dataCriacaoTabela });
        });
    }
    
